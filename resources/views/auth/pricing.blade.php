                                    <script>
                                        document.getElementById('pricing_toggle').addEventListener('change', function() {
                                            let isChecked = this.checked;
                                    
                                            // Get all elements with class 'monthly_pricing' and 'annual_pricing'
                                            let monthlyPricings = document.querySelectorAll('#monthly_pricing');
                                            let annualPricings = document.querySelectorAll('#annual_pricing');
                                    
                                            // Toggle display for monthly pricing
                                            monthlyPricings.forEach(el => {
                                                el.style.display = isChecked ? 'none' : 'block';
                                            });
                                    
                                            // Toggle display for annual pricing
                                            annualPricings.forEach(el => {
                                                el.style.display = isChecked ? 'block' : 'none';
                                            });
                                        });
                                    </script>



                                    <script>
                                        document.querySelectorAll('.select-pricing-btn').forEach(button => {
