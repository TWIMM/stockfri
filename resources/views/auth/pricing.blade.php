                                                let pricingId = this.getAttribute('data-pricing-id');

                                                fetch("{{ route('pricing.select') }}", {
                                                    method: "POST",
                                                    headers: {
                                                        "Content-Type": "application/json",
                                                        "X-CSRF-TOKEN": "{{ csrf_token() }}"
                                                    },
                                                    body: JSON.stringify({ pricing_id: pricingId })
                                                })
